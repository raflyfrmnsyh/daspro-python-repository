# Membuat program untuk mengetahui luas Segitiga
print("=========================================\n")
print("PROGRAM MENGHITUNG LUAS SEGITIGA\n")
print("=========================================\n")

alas = int(input("Masukan Nilai Alas : ")) # Mengambil nilai alas
tinggi = int(input("Masukan Nilai Tinggi : ")) # Mengambil nilai tinggi
luas = (alas / 2) * tinggi # Rumus Luas segitiga adalah ( 1/2 alas x tinggi ) 
print("\n=========================================\n")
print(f"Luas dari nilai yang anda inputkan adalah = {luas}") # Menampilkan hasil
print("\n=========================================\n")